package com.sonicle.webtop.drm.model;

import java.util.ArrayList;
import java.util.List;

/**
 *
 * @author lssndrvs
 */
public class DrmGroup {

	public String groupId;
	public String domainId;
	public String groupCategory;
	public String name;
	public String description;
	public String groupType;

	public List<DrmGroupUserAssociation> associatedUsers = new ArrayList();

	public String getGroupId() {
		return groupId;
	}

	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}

	public String getDomainId() {
		return domainId;
	}

	public void setDomainId(String domainId) {
		this.domainId = domainId;
	}

	public String getGroupCategory() {
		return groupCategory;
	}

	public void setGroupCategory(String groupCategory) {
		this.groupCategory = groupCategory;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getGroupType() {
		return groupType;
	}

	public void setGroupType(String groupType) {
		this.groupType = groupType;
	}

	public List<DrmGroupUserAssociation> getAssociatedUsers() {
		return associatedUsers;
	}

	public void setAssociatedUsers(List<DrmGroupUserAssociation> associatedUsers) {
		this.associatedUsers = associatedUsers;
	}

}
